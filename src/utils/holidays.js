/**
 * Get Thai holiday name for a given Date object.
 * @param {Date} date 
 * @returns {string|null} Holiday name if it's a holiday, else null
 */
export const getThaiHoliday = (date) => {
  if (!date) return null;
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  const year = date.getFullYear();
  const mmdd = `${month}-${day}`;

  if (dynamicHolidays[year] && dynamicHolidays[year][mmdd]) {
    return dynamicHolidays[year][mmdd];
  }
  if (fixedHolidays[mmdd]) {
    return fixedHolidays[mmdd];
  }
  return null;
};
